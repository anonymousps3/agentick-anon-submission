# Per-dataset split row counts (from HF dataset_info; matches Parquet contents).
SPLITS = {
    "agentick-oracle-trajectories-120k": {"train": 120465, "test": 120430},
    "agentick-oracle-trajectories-250k": {"train": 251076, "test": 252123},
    "agentick-oracle-trajectories-500k": {"train": 505216, "test": 252123},
}
SCALES = {
    "agentick-oracle-trajectories-120k": "120k-step",
    "agentick-oracle-trajectories-250k": "250k-step",
    "agentick-oracle-trajectories-500k": "500k-step",
}


def fetch_hf_croissant(name: str) -> dict:
    url = f"https://huggingface.co/api/datasets/{OWNER}/{name}/croissant"
    with urllib.request.urlopen(url) as resp:
        return json.loads(resp.read())


def build(name: str) -> dict:
    base = fetch_hf_croissant(name)

    base["@context"]["rai"] = "http://mlcommons.org/croissant/RAI/"
    base["@context"]["prov"] = "http://www.w3.org/ns/prov#"
    base["@context"].setdefault("examples", {"@id": "cr:examples", "@type": "@json"})
    base["@context"].setdefault("samplingRate", "cr:samplingRate")
    base["@context"].setdefault("equivalentProperty", "cr:equivalentProperty")

    base["description"] = DESCRIPTION_TEMPLATE.format(
        scale=SCALES[name],
        train_examples=SPLITS[name]["train"],
        test_examples=SPLITS[name]["test"],
    )
    base["license"] = "https://spdx.org/licenses/MIT.html"
    base["citeAs"] = CITE_AS
    base["version"] = VERSION
    base["datePublished"] = DATE_PUBLISHED
    base["creator"] = {"@type": "Person", "name": "Anonymous Author"}
    base["sameAs"] = GITHUB_REPO
    base["keywords"] = [
        "reinforcement-learning", "imitation-learning", "behavior-cloning",
        "benchmark", "ai-agents", "gridworld", "synthetic", "oracle-trajectories",
    ]

    for field, value in RAI.items():
        base[f"rai:{field}"] = value

    base["rai:hasSyntheticData"] = True
    base["prov:wasGeneratedBy"] = {
        "@type": "prov:Activity",
        "prov:label": "Oracle trajectory collection",
        "prov:description": (
            "Each row was generated by running a deterministic per-task "
            "oracle policy in a procedurally-generated Agentick environment "
            "and recording the resulting (observation, action, reward, "
            "done) tuple. Train and test splits use disjoint deterministic "
            "seeds from agentick.leaderboard.seeds.generate_task_seeds."
        ),
        "prov:used": (
            f"{GITHUB_REPO}/blob/main/examples/data_and_finetuning/"
            "collect_oracle_trajectories.py"
        ),
    }

    return base


def main():
    out_dir = Path(__file__).parent
    for name in DATASETS:
        doc = build(name)
        path = out_dir / f"{name}.croissant.json"
        with open(path, "w") as f:
            json.dump(doc, f, indent=2)
            f.write("\n")
        print(f"wrote {path}")


if __name__ == "__main__":
    main()
