"""Generate Croissant 1.1 metadata (with RAI extension) for the three published
Agentick oracle-trajectory datasets on HuggingFace.

Produces one *.croissant.json per dataset in this directory. The base
distribution / recordSet structure is fetched from HuggingFace's
auto-generated Croissant (validated by HF) so we inherit a valid file/field
schema; we then layer on hand-curated description, license, citation, and
the full Croissant RAI extension fields required by the 2026 CFP.

Run:  python croissant/_generate.py
Validate:  for f in croissant/*.croissant.json; do mlcroissant validate --jsonld "$f"; done
"""

